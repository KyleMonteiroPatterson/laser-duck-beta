game.onUpdate(function() {
    //check for a press, if press spawn laser from mouth and move it
    if (controller.up.isPressed()) {
        controller.moveSprite(MissileShooterNormal, 0, 100)
        controller.moveSprite(InvisMouthLaser, 0, 100)
        if (MissileShooterNormal.y <= TopBoundaryPoint.y) {
            controller.moveSprite(MissileShooterNormal, 0, 0)
            controller.moveSprite(InvisMouthLaser, 0, 0)
        }
    }
    if (controller.down.isPressed()) {
        controller.moveSprite(MissileShooterNormal, 0, 100)
        controller.moveSprite(InvisMouthLaser, 0, 100)
        if (MissileShooterNormal.y >= BottomBoundaryPoint.y) {
            controller.moveSprite(MissileShooterNormal, 0, 0)
            controller.moveSprite(InvisMouthLaser, 0, 0)
        }
    }
    //change difficulty depending on score
    if (Hits == 10) {
        difficulty -= 500
    }
    else if (Hits == 20) {
        difficulty -= 500
    }
    else if (Hits == 30) {
        difficulty -= 490
    }
})

